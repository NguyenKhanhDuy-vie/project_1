void StartDefaultTask(void const * argument)
{
  RTC_TimeTypeDef sTime;
  RTC_DateTypeDef sDate;
  char timeStr[20];

  for(;;)
  {
    HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
    HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN); // Ph?i g?i d? clear flag RTC

    sprintf(timeStr, "Time:%02d:%02d", sTime.Hours, sTime.Minutes);

    SSD1306_GotoXY(0, 0); // Dòng d?u tiên
		SSD1306_ClearArea(0, 0, 128, 15);
    SSD1306_Puts(timeStr,&Font_11x18, SSD1306_COLOR_WHITE);
    SSD1306_UpdateScreen();

    osDelay(60000);
  }
}
void StartTask02(void const * argument)
{
  

  HAL_TIM_Base_Start(&htim1); // Timer d? t?o microDelay

  for(;;)
  {
    if(DHT22_Start())
    {
      RH1 = DHT22_Read();
      RH2 = DHT22_Read();
      TC1 = DHT22_Read();
      TC2 = DHT22_Read();
      SUM = DHT22_Read();
      CHECK = RH1 + RH2 + TC1 + TC2;

      if (CHECK == SUM)
      {
        if (TC1>127) // If TC1=10000000, negative temperature
        {
          tCelsius = (float)TC2/10*(-1);
        }
        else
        {
          tCelsius = (float)((TC1<<8)|TC2)/10;
        }
     
        RH = (float) ((RH1<<8)|RH2)/10;
        SSD1306_GotoXY (0, 20);
        RHI = RH;  // Relative humidity integral
        RHD = RH*10-RHI*10; // Relative humidity decimal
        sprintf(strCopy,"Hump:%d.%d %%  ", RHI, RHD);
        SSD1306_Puts (strCopy, &Font_11x18, SSD1306_COLOR_WHITE);
        SSD1306_GotoXY (0, 40);
        if (tCelsius < 0)
        {
          TCI = tCelsius *(-1);  // Celsius integral
          TCD = tCelsius*(-10)-TCI*10; // Celsius decimal
          sprintf(strCopy,"Temp:-%d.%d C   ", TCI, TCD);
        }
        else
        {
          TCI = tCelsius;  // Celsius integral
          TCD = tCelsius*10-TCI*10; // Celsius decimal
          sprintf(strCopy,"Temp:%d.%d C   ", TCI, TCD);
        }


        SSD1306_Puts (strCopy, &Font_11x18, SSD1306_COLOR_WHITE);
        SSD1306_UpdateScreen();
      }
    }

    osDelay(2000);
  }
}
